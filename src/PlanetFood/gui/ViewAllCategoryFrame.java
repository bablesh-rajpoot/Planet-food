     public void showDataInTable()
    {
        try
            
        {
           ArrayList<Category>catList=CategoryDao.getAlldata();
           if(catList.isEmpty()==true)
           {
               JOptionPane.showMessageDialog(null,"Sorry! No category Present","Error",JOptionPane.INFORMATION_MESSAGE);
           }
              else
           {
               Object[]rows=new Object[2];
               DefaultTableModel model=( DefaultTableModel)jtCategorytable.getModel();
               for(Category p:catList)
               {
                   rows[0]=p.getCatId();
                   rows[1]=p.getCatName();
                  
                model.addRow(rows);
               }
           }
        }
        catch(Throwable th)
        {
          th.printStackTrace();
        }
    }

