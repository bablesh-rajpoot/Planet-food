    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        int result=validateInput();
        if(result==-1)
        {
            JOptionPane.showConfirmDialog(null,"Please Input UserId ","Input Error!",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        try
        {   
             String str=txtUserId.getText();
             userList=UserDao.searchCashier(str);
             Object[]rows=new Object[3];
             DefaultTableModel model=( DefaultTableModel)jtcashier.getModel();
             boolean found=false;
             for(User us:userList)
              
             {
                 if(str.equals(us.getUserid()))
                 { 
                      rows[0]=us.getUserid();
                      rows[1]=us.getUserName();
                      rows[2]=us.getEmpId();
                      model.addRow(rows);
                      found=true;
                      break;
                 }
             }
             if(found==false)
             {
                 JOptionPane.showMessageDialog(null,"Does Not Exit UserId\n Please Valid UserId");
             }
              
                  
                  
              } 
      catch(Throwable th)
        {
            th.printStackTrace();
        }
       
    }//GEN-LAST:event_btnsearchActionPerformed

