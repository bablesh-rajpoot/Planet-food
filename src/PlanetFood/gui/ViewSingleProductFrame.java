    private void jcProdIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcProdIdActionPerformed
        // TODO add your handling code here:
        try
        {
          String catName=jcProdCat.getSelectedItem().toString();
          String catId=categories.get(catName).toString();
          if(jcProdId.getSelectedItem()==null)
          {
              return;
          }
          
           Product p=productList.get(jcProdId.getSelectedItem().toString());
           txtProdName.setText(p.getProdName());
           txtProdPrice.setText(p.getProdPrice()+"");
        }
        catch(Throwable th)
        {
            th.printStackTrace();
        }
        
    }//GEN-LAST:event_jcProdIdActionPerformed

