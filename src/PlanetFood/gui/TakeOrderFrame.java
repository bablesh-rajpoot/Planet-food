    public void loadProductDetails()
    {
       try
       {
           categories=CategoryDao.getAllCategoryId();
           for(String catName:categories.keySet())
           jcProdCat.addItem(catName);
           
           ordId=OrdersDao.getNewId();
           txtOrderId.setText(ordId);
          
           do
           {
              String discountStr=null;
              discountStr=JOptionPane.showInputDialog("How much discount is offered ? ");
             try
             {
               discountPer=Double.parseDouble(discountStr);
               lblDiscount.setText("Discount("+discountPer+"%)");
             
               break;
             }
             
             
             catch(NumberFormatException th)
             {
                 JOptionPane.showMessageDialog(null,"Please Set numeric value for discount","Error!",JOptionPane.INFORMATION_MESSAGE);
                 th.printStackTrace();
             }
           }while(true);
           
      }
       catch(SQLException ex)
       {
           JOptionPane.showMessageDialog(null,"Exception in db","Exception!",JOptionPane.INFORMATION_MESSAGE);
           ex.printStackTrace();
       }
       
    
    }

    

