   public void showDataInTable()
    {
        try
            
        {
           ArrayList<Product>prodList=ProductDao.getAlldata();
           ArrayList<OrderDetail>ordList=OrderDetailDao.getAlldata();
           if(ordList.isEmpty()==true)
           {
               JOptionPane.showMessageDialog(null,"Sorry! No OrderDetails  Present","Error",JOptionPane.INFORMATION_MESSAGE);
           }
              else
           {
               Object[]rows=new Object[5];
               DefaultTableModel model=( DefaultTableModel)jtproductDetails.getModel();
               for(OrderDetail od:ordList)
               {
                   Product p=productList.get(jcProdId.getSelectedItem().toString());
                   rows[0]=od.getProdId();
                   rows[1]=p.getProdName();
                   rows[2]=p.getProdPrice();
                   rows[3]=od.getQuantity();
                   rows[4]=od.getCost();
                   model.addRow(rows);
               }
           }
        }
        catch(Throwable th)
        {
          th.printStackTrace();
        }
    }
