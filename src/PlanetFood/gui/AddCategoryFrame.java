    private void jcaddCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcaddCategoryActionPerformed
        // TODO add your handling code here:
        try
        {
           Category c=new Category();
           c.setCatId(jcCatId.getSelectedItem().toString());
           c.setCatName(jcCatName.getSelectedItem().toString());
            boolean status=CategoryDao.addcategories(c);
            if(status)
            {
                JOptionPane.showMessageDialog(null,"Category successfully Added!","Success!",JOptionPane.INFORMATION_MESSAGE);
               
            }
            else
            JOptionPane.showMessageDialog(null,"category not Added!","Adding product Denied!",JOptionPane.INFORMATION_MESSAGE);

        }
        catch(Throwable th)
        {
            th.printStackTrace();
        }
    }//GEN-LAST:event_jcaddCategoryActionPerformed

